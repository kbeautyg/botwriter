def _anti_patterns_block() -> str:
    lines = []
    for ap in ANTI_PATTERNS:
        lines.append(
            f"— {ap.name}: {ap.description}\n"
            f"   плохой пример: «{ap.example_bad}»\n"
            f"   почему: {ap.why_bad}"
        )
    return "\n".join(lines)


def _banned_words_block() -> str:
    return ", ".join(f"«{w}»" for w in BANNED_WORDS)


def _bad_post_block() -> str:
    """Negative example: пример того, как ChatGPT обычно пишет — и что Артём отверг."""
    if not SEED_BAD_POSTS:
        return ""
    bad = SEED_BAD_POSTS[0]
    return (
        "═══════════════════════════════════════\n"
        "АНТИ-ЭТАЛОН (так ChatGPT написал, Артём прямо забраковал)\n"
        "═══════════════════════════════════════\n"
        f"--- ПЛОХО (жанр: {bad.genre}) ---\n"
        f"{bad.text}\n"
        f"--- /ПЛОХО ---\n"
        f"Почему плохо: {bad.why_bad}\n"
        "Никогда не пиши в такой манере. Никаких заголовков «Кто Мы?», "
        "буллетов с одинаковыми эмодзи, тройки прилагательных, "
        "концовок «Оставайтесь, будет интересно»."
    )


