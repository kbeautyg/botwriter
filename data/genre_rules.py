def format_for_writer(genre: str) -> str:
    """Готовый блок для подкладки в Writer-промпт."""
    rules = GENRE_RULES.get(genre)
    if not rules:
        return ""
    rules_block = "\n".join(f"  • {r}" for r in rules)
    universal_block = "\n".join(f"  • {r}" for r in UNIVERSAL_RULES)
    return (
        f"═══ ЖАНРОВЫЕ ПРАВИЛА для «{genre}» (обязательные) ═══\n"
        f"{rules_block}\n\n"
        f"═══ УНИВЕРСАЛЬНЫЕ ПРАВИЛА (для любого жанра) ═══\n"
        f"{universal_block}"
    )


def format_for_critic(genre: str) -> str:
    """Готовый блок для подкладки в Critic-промпт — что проверять."""
    rules = GENRE_RULES.get(genre)
    if not rules:
        return ""
    rules_block = "\n".join(f"  • {r}" for r in rules)
    universal_block = "\n".join(f"  • {r}" for r in UNIVERSAL_RULES)
    return (
        f"═══ ЖАНРОВЫЕ ТРЕБОВАНИЯ для «{genre}» (проверяй соответствие) ═══\n"
        f"{rules_block}\n\n"
        f"═══ УНИВЕРСАЛЬНЫЕ ТРЕБОВАНИЯ ═══\n"
        f"{universal_block}\n\n"
        "Если пост нарушает явное правило этого жанра — снижай anti_ai и в must_fix укажи конкретику."
    )


def format_for_planner() -> str:
    """Готовый блок для Planner — сводка всех жанров и их правил.
    Planner использует это чтобы понять, в какой жанр попасть и что это повлечёт.
    """
    blocks = []
    for genre, rules in GENRE_RULES.items():
        rules_short = "; ".join(rules[:3])  # первые 3 правила — самые важные
        blocks.append(f"  {genre}: {rules_short}")
    return "═══ ЖАНРЫ И ИХ КЛЮЧЕВЫЕ ПРАВИЛА ═══\n" + "\n".join(blocks)
